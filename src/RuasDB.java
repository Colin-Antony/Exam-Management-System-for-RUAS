    private void RecordPaymentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RecordPaymentActionPerformed
        String student_id=jTextField14.getText();
        double current_fees=0.0; 
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            String sql="select * from student_marks where student_id='"+student_id+"'";
            connection=DriverManager.getConnection(dbUrl,username,password);
            preparedStatement=connection.prepareStatement(sql);
            resultSet=preparedStatement.executeQuery();
            while(resultSet.next())
            {
                current_fees=resultSet.getDouble("Pending_Fees");
            }
            double current_payment=Double.parseDouble(jTextField23.getText());
            double updated_fees=current_fees-current_payment;
            if (updated_fees==0){
                RuasDB.infoBox("Fees Fully Paid!","Payment Successful!");
                String sql2="UPDATE student_marks SET Pending_Fees=0 WHERE S"
                        + "tudent_id='"+student_id+"'";
                preparedStatement=connection.prepareStatement(sql2);
                preparedStatement.executeUpdate();
            }
            else if(updated_fees<0)
                RuasDB.infoBox("You are paying more than what is owed!!!","Payment Failed!");
            else{
                RuasDB.infoBox("You still owe rupees "+updated_fees,"Payment Succesful!");
                String sql2="UPDATE student_marks SET Pending_F"
                        + "ees="+updated_fees+" WHERE Student_id='"+student_id+"'";
                preparedStatement=connection.prepareStatement(sql2);
                preparedStatement.executeUpdate();
            }
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_RecordPaymentActionPerformed

