    private void jButton15ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton15ActionPerformed
        String student_id=jTextField11.getText();
        double M1_CA=0.0;
        double M2_CA=0.0;
        double M3_CA=0.0;
        double M4_CA=0.0;
        double attendance=0.0;
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            String sql="select * FROM student_marks WHERE Student_id= \""+student_id+"\"  ";
            connection=DriverManager.getConnection(dbUrl,username,password);
            preparedStatement= connection.prepareStatement(sql);
            resultSet=preparedStatement.executeQuery();
            while(resultSet.next())
            {
                M1_CA=resultSet.getDouble("M1_CA");
                M2_CA=resultSet.getDouble("M2_CA");
                M3_CA=resultSet.getDouble("M3_CA");
                M4_CA=resultSet.getDouble("M1_CA");
                attendance=resultSet.getDouble("Attendance_P");
            }
            /*required marks to qualify is 40 percent therefore total should be greater
            than or equal to 160. Attendance should be abouve 75*/
            if(((M1_CA+M2_CA+M3_CA+M4_CA)>=160)&&(attendance>=75)){
                jLabel28.setText("ELIGIBLE!!");
            }
            else{
                jLabel28.setText("NOT ELIGIBLE!!");
            }

        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jButton15ActionPerformed

